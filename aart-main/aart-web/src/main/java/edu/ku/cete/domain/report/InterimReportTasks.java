package edu.ku.cete.domain.report;

public class InterimReportTasks {
	private String response;
	private String responseNo;
	private String responsePercent;

	public String getResponse() {
		return response;
	}

	public void setResponse(String response) {
		this.response = response;
	}

	public String getResponseNo() {
		return responseNo;
	}

	public void setResponseNo(String responseNo) {
		this.responseNo = responseNo;
	}

	public String getResponsePercent() {
		return responsePercent;
	}

	public void setResponsePercent(String responsePercent) {
		this.responsePercent = responsePercent;
	}
}