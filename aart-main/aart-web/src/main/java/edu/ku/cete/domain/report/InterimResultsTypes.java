package edu.ku.cete.domain.report;

public class InterimResultsTypes {

	private String itemType;

	private String scoringType;

	private String pointValue;

	private String correctPercentage;

	private String incorrectPercentage;

	private String alignment;
	public String getItemType() {
		return itemType;
	}

	public void setItemType(String itemType) {
		this.itemType = itemType;
	}

	public String getScoringType() {
		return scoringType;
	}

	public void setScoringType(String scoringType) {
		this.scoringType = scoringType;
	}

	public String getPointValue() {
		return pointValue;
	}

	public void setPointValue(String pointValue) {
		this.pointValue = pointValue;
	}

	public String getCorrectPercentage() {
		return correctPercentage;
	}

	public void setCorrectPercentage(String correctPercentage) {
		this.correctPercentage = correctPercentage;
	}

	public String getIncorrectPercentage() {
		return incorrectPercentage;
	}

	public void setIncorrectPercentage(String incorrectPercentage) {
		this.incorrectPercentage = incorrectPercentage;
	}

	public String getAlignment() {
		return alignment;
	}

	public void setAlignment(String alignment) {
		this.alignment = alignment;
	}

}
