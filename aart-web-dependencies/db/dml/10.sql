CREATE OR REPLACE FUNCTION insertRecordsInStudentTestSection() RETURNS INTEGER AS 
$BODY$
DECLARE  
    studentTestId             public.studentstests.id%TYPE;
    testSectionId             public.testsection.id%TYPE;  
    tableCount	           integer = 0;
    tableCursor CURSOR FOR  
         select st.id, ts.id from studentstests st join testsection ts on st.testid= ts.testid;

BEGIN
RAISE INFO '---------Enter---------------';   
OPEN tableCursor;   
	LOOP
		
		FETCH tableCursor INTO studentTestId, testSectionId;  
		EXIT WHEN NOT FOUND ;  
			--RAISE INFO '%     %', studentTestId, testSectionId;   
			
			EXECUTE 'INSERT INTO studentstestsections(' || 'studentstestid' || ',' || 'testsectionid' || ',' || 'createduser' || ',' || 'modifieduser' || ') VALUES (' || studentTestId || ',' || testSectionId || ',(select id from aartuser where username=''cetesysadmin''),(select id from aartuser where username=''cetesysadmin''))';
			 			
			tableCount = tableCount + 1; 
		
	END LOOP;
CLOSE tableCursor;  
RAISE INFO '---------Exit with %--------------- ', tableCount;   
RETURN tableCount;  
EXCEPTION 
            WHEN undefined_object THEN
            RAISE NOTICE 'caught exception %', tableCount;
            RETURN tableCount;
END
$BODY$
LANGUAGE plpgsql;

